package Modelo;

/**
 *
 * @author Diego Velez
 */
public class Empresa {
    
    private String empre_nit;
    private String empre_razon_social;
    private String empre_repre_legal;
    private String empre_dir;
    private String empre_logo;

    public Empresa() {
    }

    public String getEmpre_nit() {
        return empre_nit;
    }

    public void setEmpre_nit(String empre_nit) {
        this.empre_nit = empre_nit;
    }

    public String getEmpre_razon_social() {
        return empre_razon_social;
    }

    public void setEmpre_razon_social(String empre_razon_social) {
        this.empre_razon_social = empre_razon_social;
    }

    public String getEmpre_repre_legal() {
        return empre_repre_legal;
    }

    public void setEmpre_repre_legal(String empre_repre_legal) {
        this.empre_repre_legal = empre_repre_legal;
    }

    public String getEmpre_dir() {
        return empre_dir;
    }

    public void setEmpre_dir(String empre_dir) {
        this.empre_dir = empre_dir;
    }

    public String getEmpre_logo() {
        return empre_logo;
    }

    public void setEmpre_logo(String empre_logo) {
        this.empre_logo = empre_logo;
    }
    
    
    
}
