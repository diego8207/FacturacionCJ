    private void tblListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListadoMouseClicked

        btnGuardar.setText("Editar");
        habilitar();
        btnEliminar.setEnabled(true);
        accion = "editar";

        int fila = tblListado.rowAtPoint(evt.getPoint());
        txtid_persona.setText(tblListado.getValueAt(fila, 0).toString());
        txtnombre.setText(tblListado.getValueAt(fila, 1).toString());
        txtamaterno.setText(tblListado.getValueAt(fila, 2).toString());
        txtapaterno.setText(tblListado.getValueAt(fila, 3).toString());
        cmbtipodocumento.setSelectedItem(tblListado.getValueAt(fila, 4).toString());
        txtnumdocumento.setText(tblListado.getValueAt(fila, 5).toString());
        txtdireccion.setText(tblListado.getValueAt(fila, 6).toString());
        txttelefono.setText(tblListado.getValueAt(fila, 7).toString());
        txtemail.setText(tblListado.getValueAt(fila, 8).toString());
        txtcodcliente.setText(tblListado.getValueAt(fila, 9).toString());
        txtdescuento.setText(tblListado.getValueAt(fila, 10).toString());
        
    }//GEN-LAST:event_tblListadoMouseClicked

