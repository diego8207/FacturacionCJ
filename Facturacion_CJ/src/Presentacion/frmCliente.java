    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtnombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar nombre del Cliente");
            txtnombre.requestFocus();
            return;
        }
        if (txtamaterno.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el Apellido Materno");
            txtamaterno.requestFocus();
            return;
        }
        if (txtapaterno.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el apellido Paterno");
            txtapaterno.requestFocus();
            return;
        }
        if (txtnumdocumento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripcion del producto");
            cmbtipodocumento.requestFocus();
            return;
        }
          
        Cliente cliente = new Cliente();
        fCliente func = new fCliente();

        cliente.setNOMBRE(txtnombre.getText());
        cliente.setAMATERNO(txtamaterno.getText());
        cliente.setAPATERNO(txtapaterno.getText());
        int seleccionado = cmbtipodocumento.getSelectedIndex();
        cliente.setTIPO_DOCUMENTO((String)cmbtipodocumento.getItemAt(seleccionado));
        cliente.setNUMERO_DOCUMENTO(txtnumdocumento.getText());
        cliente.setDIRECCION(txtdireccion.getText());
        cliente.setTELEFONO(txttelefono.getText());
        cliente.setEMAIL(txtemail.getText());
        cliente.setCODIGO_CLIENTE(txtcodcliente.getText());
        cliente.setDESCUENTO(txtdescuento.getText());

        
        
        if (accion.equals("guardar")) {
            if (func.insertar(cliente)) {
                JOptionPane.showMessageDialog(rootPane, "El Cliente fue registrado satisfactoriamente");
                mostrar("");
                inhablitar();
            }
        } else if (accion.equals("editar")) {
            cliente.setIDPERSONA(Integer.parseInt(txtid_persona.getText()));
            if (func.editar(cliente)) {
                JOptionPane.showMessageDialog(rootPane, "El Cliente fue editado satisfactoriamente");
                mostrar("");
                inhablitar();
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

