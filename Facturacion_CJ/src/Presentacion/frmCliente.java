    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtid_persona.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar el Cliente?", "Confirmar", 2);
            if (confirmacion == 0) {
                fCliente func = new fCliente();
                Cliente cliente = new Cliente();
                cliente.setIDPERSONA(Integer.parseInt(txtid_persona.getText()));
                func.eliminar(cliente);
                mostrar("");
                inhablitar();

            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

