    public void agregarItem(){
            registro[0] = txtcodigoproducto.getText();
            registro[1] = cbxnombreproducto.getSelectedItem().toString();
            registro[2] = txtcantproducto.getText();
            registro[3] = txtprecio.getText();
            registro[4] = valorVentaItem();
            modelo.addRow(registro);
            tblDetFactura.setModel(modelo);
            
    }
    
    public String valorVentaItem(){
        int valor = 0;
        valor = Integer.parseInt(txtprecio.getText()) * Integer.parseInt(txtcantproducto.getText());
        String total = String.valueOf(valor);
        totalfact = totalfact + valor;
        NumberFormat nf = NumberFormat.getCurrencyInstance(Locale.getDefault());
        
        txtTotalFact.setText(String.valueOf(nf.format(totalfact)));
        return total;
    }
    
