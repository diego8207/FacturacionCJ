    private void saberDatosCliente(String nombreCliente) {
        try {
            fPersona persona = new fPersona();
            ResultSet rs = persona.ConsultarPorNombreCliente(nombreCliente);
            while (rs.next()) {
                globalDireccion = rs.getString("DIRECCION");
                globalDocumento = rs.getString("NUMERO_DOCUMENTO");
                globalTelefono = rs.getString("TELEFONO");
            }
            txtdireccion.setText(globalDireccion);
            txtdocumento.setText(globalDocumento);
            lbltelefono.setText(globalTelefono);

        } catch (SQLException e) {
            System.out.println(e);
        }
    }
    
    private void saberDatosProducto(String nombreProducto){
        try {
            
            fProducto producto = new fProducto();
            ResultSet rs = producto.ConsultarProNombreProducto(nombreProducto);
            while (rs.next()) {                
                int stock = Integer.parseInt(rs.getString("STOCK"));
                if (stock > 0) {
                    globalCodProducto = rs.getString("CODIGO_PRODUCTO");//falta el id para almacenarlo
                    globalPrecioProducto = rs.getString("PRECIO_VENTA_PRODUCTO");
                    txtcodigoproducto.setText(globalCodProducto);
                    txtprecio.setText(globalPrecioProducto);
                }else{
                    //poner un joptionpane que no hay stock
                }
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

    public void CargarComboClienteAutocompletar() {
        cbxnombrecliente.getEditor().getEditorComponent().addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent evt) {
                String cadenaEscrita = cbxnombrecliente.getEditor().getItem().toString();
                if (evt.getKeyCode() >= 65 && evt.getKeyCode() <= 90 || evt.getKeyCode() >= 96 && evt.getKeyCode() <= 105 || evt.getKeyCode() == 8) {
                    cbxnombrecliente.setModel(p.getListaClientes(cadenaEscrita));
                    if (cbxnombrecliente.getItemCount() > 0) {
                        cbxnombrecliente.showPopup();
                        if (evt.getKeyCode() != 8) {
                            ((JTextComponent) cbxnombrecliente.getEditor().getEditorComponent())
                                    .select(cadenaEscrita.length(), cbxnombrecliente.getEditor().getItem().toString().length());
                        } else {
                            cbxnombrecliente.getEditor().setItem(cadenaEscrita);
                        }
                    }
                }
            }

        });
    }

    public void CargarComboProductosAutocompletar() {
        
        
        cbxnombreproducto.getEditor().getEditorComponent().addKeyListener(new KeyAdapter() {
            
            @Override
            public void keyReleased(KeyEvent evt) {
                String cadenaEscrita = cbxnombreproducto.getEditor().getItem().toString();
                if (evt.getKeyCode() >= 65 && evt.getKeyCode() <= 90 || evt.getKeyCode() >= 96 && evt.getKeyCode() <= 105 || evt.getKeyCode() == 8) {
                    try {
                        ResultSet rs = producto.getListaProductos(cadenaEscrita);
                        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
                        while (rs.next()) {
                            modelo.addElement(rs.getString("NOMBRE_PRODUCTO"));
                        }
                        cbxnombreproducto.setModel(modelo);
                        if (cbxnombrecliente.getItemCount() > 0) {
                            cbxnombreproducto.showPopup();
                            if (evt.getKeyCode() != 8) {
                                ((JTextComponent) cbxnombreproducto.getEditor().getEditorComponent())
                                        .select(cadenaEscrita.length(), cbxnombreproducto.getEditor().getItem().toString().length());
                            } else {
                                cbxnombreproducto.getEditor().setItem(cadenaEscrita);
                            }
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(frmFactura.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }

        });
    }

